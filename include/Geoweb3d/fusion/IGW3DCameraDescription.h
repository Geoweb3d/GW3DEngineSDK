#pragma once
#include <Geoweb3d/core/GW3DInterFace.h>

/* Primary namespace */
namespace Geoweb3d
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Description of a camera exposed via Geoweb3d Fusion. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct  GW3D_DLL IGW3DCameraDescription
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the friendly name of the camera. </summary>
		/// 
		/// <remarks>	The friendly name is the name provided by the application of creation of the
		/// 			camera.</remarks>
		///
		/// <returns>	the friendly name. </returns>
		/// 
		/// <see cref="Geoweb3d::IGW3DCamera"/>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual const char *get_FriendlyName() const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets unique identifier of the camera. </summary>
		///
		/// <remarks>	This unique identifier is generated by the Geoweb3d Fusion SDK. </remarks>
		///
		/// <returns>	the unique identifier. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual const char *get_UniqueID() const = 0;
	};
};
