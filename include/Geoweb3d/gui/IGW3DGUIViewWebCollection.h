#pragma once
#include "core/GW3DGUIInterFace.h"
#include <Geoweb3dCore/GW3DResults.h>

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Collection of 3D views in the Geoweb3d user interface. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIViewWebCollection : public IGW3DCollection< IGW3DGUIViewWebWPtr >
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Creates a new web view. </summary>
		///
		/// <param name="name">	(optional) the default url to load. </param>
		///
		/// <returns>	the new web view. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIViewWebWPtr create( const char *url = 0 ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the active web view. </summary>
		///
		/// <returns>	The active web view, or an expired pointer if no web view is active. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIViewWebWPtr get_Active ( ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Make the given web view active inthe user interface. </summary>
		///
		/// <param name="view">	The view to be made active. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual Geoweb3d::GW3DResult put_Active ( IGW3DGUIViewWebWPtr view ) = 0;
	};

}
}



