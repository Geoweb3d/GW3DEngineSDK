#pragma once
#include "../core/GW3DGUIInterFace.h"
#include <Geoweb3dCore/GW3DResults.h>

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	The collection of all items in a menu. </summary>
	///
	/// <remarks>	The collection of all menu items directly under a menu. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIMenuItemCollection : public IGW3DCollection< IGW3DGUIMenuItemWPtr >
	{

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Get the menu to which this collection belongs. </summary>
		///
		/// <returns>	The menu. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIMenuWPtr get_Menu( ) = 0;
	   
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets a menu item by id. </summary>
		///
		/// <param name="id">	The id of the requested item.	</param>
		///
		/// <returns>	the menu item, or an expired weak pointer if failed. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIMenuItemWPtr get_ById( const char * id ) const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Set item as enabled/disabled. </summary>
		///
		/// <param name="item">	The item.	</param>
		/// <param name="enabled">	Whether or not to enable the item.	</param>
		///
		/// <returns>	the menu item, or an expired weak pointer if failed. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual Geoweb3d::GW3DResult put_Enabled (Geoweb3d::GUI::IGW3DGUIMenuItemWPtr item, bool enabled) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Set item as visible/invisible. </summary>
		///
		/// <param name="id">	The id of the requested item.	</param>
		/// <param name="visible">	Whether or not to make the item visible.	</param>
		///
		/// <returns>	the menu item, or an expired weak pointer if failed. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual Geoweb3d::GW3DResult put_Visible (Geoweb3d::GUI::IGW3DGUIMenuItemWPtr item, bool visible) = 0;

	};

}
}


