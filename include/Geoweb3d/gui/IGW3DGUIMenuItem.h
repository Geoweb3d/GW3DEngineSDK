#pragma once
#include "core/GW3DGUIInterFace.h"

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	An item in a context menu. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIMenuItem
	{
		
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the unique ID for the menu. </summary>
		///
		/// <returns>	The ID. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual const char* get_Id () const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the submenu appearing under an item, if applicable. </summary>
		///
		/// <returns>	If a submenu exists, the menu.  Otherwise, an expired pointer. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIMenuWPtr get_Menu () const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Check if the menu item is a separator. </summary>
		///
		/// <remarks>	A separator acts as a divider between groups of menu items in a menu.  Separators
		///				do not have specific ids, instead they have the id "GW3DGUIMenuItemSeparator#"
		///				where '#' is derived from a counter, starting at 1.  This value is not guaranteed
		///				to always be the same, as separators are non-unique.
		///
		/// <returns>	True if the item is a separator, false if it is not. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual bool get_IsSeparator() const = 0;


	};

}
}



