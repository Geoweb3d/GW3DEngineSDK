#pragma once
#include "core/GW3DGUIInterFace.h"
#include <Geoweb3dCore/GW3DResults.h>

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Collection of 3D views in the Geoweb3d user interface. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIView3dCollection : public IGW3DCollection< IGW3DGUIView3dWPtr >
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Creates a new 3D view. </summary>
		///
		/// <param name="name">	(optional) the name. </param>
		///
		/// <returns>	the new 3D view. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIView3dWPtr create( const char *name = 0 ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the active 3D view. </summary>
		///
		/// <returns>	The active 3D view, or an expired pointer if no 3D view is active. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIView3dWPtr get_Active ( ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Make the given 3D view active in the user interface. </summary>
		///
		/// <param name="view">	The view to be made active. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual Geoweb3d::GW3DResult put_Active ( IGW3DGUIView3dWPtr view ) = 0;
	};

}
}



