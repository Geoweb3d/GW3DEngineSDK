#pragma once
#include "core/GW3DGUIInterFace.h"

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	A Web View. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIViewWeb
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the url for the page the web view should display. </summary>
		///
		/// <param name="url">	URL of the document. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_Url (const char* url) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the html code for the page the web view should display. </summary>
		///
		/// <param name="html">   	The HTML. </param>
		/// <param name="baseUrl">	Base url for relative paths inside the html. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_Html (const char* html, const char* baseUrl) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Reload the page. </summary>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_Reload () = 0;

	};
}
}



