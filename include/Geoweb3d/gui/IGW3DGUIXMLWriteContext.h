#pragma once
#include "core/GW3DGUIInterFace.h"

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Xml write context </summary>
	///
	/// <remarks>	The xml write context is provided to the plugin for adding custom XML to project, layer, 
	//              and representation files </remarks>
	/// 
	/// <see cref="OnXMLWrite"/>
	////////////////////////////////////////////////////////////////////////////////////////////////////\

	struct IGW3DGUIXMLWriteContext
	{

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Query the type of file for the XML write </summary>
		///
		/// <remarks> Returns which file type (Project, Layer, or Representation) 
		/// 		  is open for writing </remarks>
		///
		/// <returns> An enumeration of IGW3DGeoweb3dFileType indicating which file is being written</returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual Geoweb3dFileType get_FileType () = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Returns the XML buffer </summary>
		///
		/// <remarks> Returns a pointer to the character buffer containing the plugin custom XML. </remarks>
		///
		/// <returns> The pointer to the character buffer if valid, otherwise null.</returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual const char* get_XML () = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Sets the XML character buffer pointer  </summary>
		///
		/// <remarks> Archives the character buffer pointer for subsequent query. </remarks>
		///
		/// <param name="buffer"> The pointer to the character buffer. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void  put_XML (const char*) = 0;
	};

}
}



