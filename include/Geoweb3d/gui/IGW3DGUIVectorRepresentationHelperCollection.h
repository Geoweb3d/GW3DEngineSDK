#pragma once
#include "core/GW3DGUIInterFace.h"
#include <Geoweb3dCore/GW3DResults.h>

namespace Geoweb3d
{

namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Helper collection containing representations. </summary>
	///
	/// <remarks>	Representations may be added or removed from this container. 
	/// 			This is an abstract class. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIVectorRepresentationHelperCollection : public IGW3DCollection< IGW3DGUIVectorRepresentationWPtr >
	{

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Add a representation to the collection. </summary>
		///
		/// <param name="rep">	The representation to add. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual GW3DResult add( IGW3DGUIVectorRepresentationWPtr rep ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Remove a representation from the collection. </summary>
		///
		/// <param name="rep">	The representation to remove. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual GW3DResult remove( IGW3DGUIVectorRepresentationWPtr rep ) = 0;
		
	};

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Helper collection containing basic representations. </summary>
	///
	/// <remarks>	Basic representations may be added or removed from this container. 
	/// 			This is an abstract class. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct IGW3DGUIVectorRepresentationBasicHelperCollection : public IGW3DCollection< IGW3DGUIVectorRepresentationBasicWPtr >
	{

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Add a basic representation to the collection. </summary>
		///
		/// <param name="rep">	The basic representation to add. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual GW3DResult add( IGW3DGUIVectorRepresentationBasicWPtr rep ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Remove a basic representation from the collection. </summary>
		///
		/// <param name="rep">	The basic representation to remove. </param>
		///
		/// <returns>	GW3D_sOk if succeeded. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual GW3DResult remove( IGW3DGUIVectorRepresentationBasicWPtr rep ) = 0;
		
	};

}
}

