#pragma once
#include "core/GW3DGUIInterFace.h"

/* Primary namespace */
namespace Geoweb3d
{
namespace GUI
{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Line segment intersection test. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	struct  IGW3DGUILineSegmentIntersectionTest
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the enabled state. </summary>
		///
		/// <param name="enable">	true to enable, false to disable. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_Enabled( bool enable) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the enabled state. </summary>
		///
		/// <returns>	true if it succeeds, false if it fails. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual bool get_Enabled( ) const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the start and end points. </summary>
		///
		/// <param name="startpt">	The start point. </param>
		/// <param name="endpt">  	The end point. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_StartEnd( const GW3DPoint &startpt,  const GW3DPoint &endpt) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the start point. </summary>
		///
		/// <param name="startpt">	The start point. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_Start( const GW3DPoint &startpt ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Sets the end point. </summary>
		///
		/// <param name="endpt">	The end point. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual void put_End( const GW3DPoint &endpt ) = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Used to and/remove basic representations to participate. </summary>
		///
		/// <returns>	the basic vector representation collection. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIVectorRepresentationBasicHelperCollection * get_VectorRepresentationBasicCollection() = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Used to and/remove standard representations to participate. </summary>
		///
		/// <returns>	the vector representation collection. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUIVectorRepresentationHelperCollection * get_VectorRepresentationCollection() = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Container of intersection results from the last time the scene was drawn
		///				</summary>
		///
		/// <returns>	the intersection collection. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual IGW3DGUILineSegmentIntersectionReportCollection * get_IntersectionReportCollection() = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Get the frame time stamp that the intersection results were last updated </summary>
		///
		///	<remarks>	The master frame time stamp is incremented by 1 every frame. </remarks>
		///
		/// <returns>	the frame time stamp. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual unsigned get_FrameTimeStamp() const= 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Check if test is valid. </summary>
		///
		/// <returns>	true if it succeeds, false if it fails. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		virtual bool	get_IsValid() const = 0;
	};

}
}